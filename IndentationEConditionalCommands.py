#Conditional Commands
# if
x = 1
y = 1000

if x > y:
	print("x is greater than y")

if y > x:
	print("y is greater than x")

# if and else
x = 1000
y = 1

if y > x:
	print("y is greater than x")
else:
	print("x is greater than y")

# while
x = 0

while x < 5:
	print(x)
	x= x+1
	# x += 1 is same x= x+1